public class TK1_No1b {
    public static void main(String[] args) {
        String str_A = "SAYA PROGRAMMER JAVA";
        String str_B = "SAYA SUKA PEMROGRAMAN";
        String strUpper_A = str_A.toLowerCase();
        String strUpper_B = str_B.toLowerCase();
        System.out.println("Original String A : "+str_A);
        System.out.println("Original String B : "+str_B);
        System.out.println("\nString A change to lower case: \n"+ strUpper_A );
        System.out.println("\nString B change to lower case: \n"+ strUpper_B);
    }
}
