public class TK1_No1a {
    public static void main(String[] args) {
        String str_A = "Saya Programmer Java";
        String str_B = "Saya suka Pemrograman";
        String strUpper_A = str_A.toUpperCase();
        String strUpper_B = str_B.toUpperCase();
        System.out.println("Original String A : "+str_A);
        System.out.println("Original String B : "+str_B);
        System.out.println("\nString A change to upper case: \n"+ strUpper_A );
        System.out.println("\nString B change to upper case: \n"+ strUpper_B);
    }
}
